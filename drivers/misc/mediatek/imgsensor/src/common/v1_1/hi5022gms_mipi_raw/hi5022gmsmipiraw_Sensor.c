static kal_uint32 set_test_pattern_mode(kal_bool enable)
{
	pr_debug("set_test_pattern_mode enable: %d", enable);

	if (test_pattern_solid == 0) {
		test_pattern_enb = read_cmos_sensor(0x0B04);
		test_pattern_solid = read_cmos_sensor(0x0C0A);
		test_pattern_isp = read_cmos_sensor(0x0B06);
	}

	if (enable) {
		write_cmos_sensor(0x0B04, (test_pattern_enb & ~0x15FE) | 0x0001); //disable B[12], B[10], B[8:1], // Test Pattern Enb B[0]
		write_cmos_sensor(0x0C0A, test_pattern_solid | 0x0101); //Solid color
		write_cmos_sensor(0x0B06, test_pattern_isp & ~0x0500);
	} else {
		write_cmos_sensor(0x0B04, test_pattern_enb);
		write_cmos_sensor(0x0C0A, test_pattern_solid);
		write_cmos_sensor(0x0B06, test_pattern_isp);
	}

	spin_lock(&imgsensor_drv_lock);
	imgsensor.test_pattern = enable;
	spin_unlock(&imgsensor_drv_lock);
	return ERROR_NONE;
}

